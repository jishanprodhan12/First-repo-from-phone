// QN-025: অ্যারে থেকে ডুপ্লিকেট অপসারণ (Remove Duplicates)।
// 'data' নামে একটি অ্যারে আছে যেখানে কিছু ডুপ্লিকেট সংখ্যা রয়েছে।
//const data = [10, 20, 30, 10, 40, 50, 20, 60, 30];

// 1. জাভাস্ক্রিপ্টের সবচেয়ে সহজ এবং আধুনিক পদ্ধতি (যেমন Set অবজেক্ট এবং স্প্রেড অপারেটর) ব্যবহার করে 
//    'data' অ্যারে থেকে সমস্ত ডুপ্লিকেট সংখ্যা অপসারণ করুন।
// 2. নতুন ইউনিক অ্যারেটির নাম দিন 'uniqueData'।
// 3. 'uniqueData' অ্যারেটি কনসোলে প্রিন্ট করার জন্য প্রয়োজনীয় কোড লিখুন।

const data = [10, 20, 30, 10, 40, 50, 20, 60, 30];

const uniqueData = [...new Set(data)];

console.log("Unique Data:", uniqueData);

console.log("Original Data (Unchanged):", data);
