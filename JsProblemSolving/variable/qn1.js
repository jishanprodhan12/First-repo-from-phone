/**
 * QN-001: ভেরিয়েবল ঘোষণা ও কনসোলে প্রিন্ট।
 * * জাভাস্ক্রিপ্টে আধুনিক ভেরিয়েবল ডিক্লেয়ার করার পদ্ধতি: let এবং const ব্যবহার করা।
 * var ব্যবহার করা উচিত নয়, কারণ এর স্কোপিং (scoping) নিয়ে সমস্যা রয়েছে।
 */

// 1. let ব্যবহার করে ভেরিয়েবল ঘোষণা:
// 'let' ব্যবহার করা হয় যখন ভেরিয়েবলের মান পরবর্তীতে পরিবর্তন হতে পারে।
let age = 25;
console.log("let ব্যবহার করে বয়স:", age); 

// উদাহরণ: let দিয়ে ঘোষিত ভেরিয়েবলের মান পরিবর্তন করা যায়।
age = 26;
console.log("বয়স আপডেট:", age); 


// 2. const ব্যবহার করে ভেরিয়েবল ঘোষণা:
// 'const' ব্যবহার করা হয় যখন ভেরিয়েবলের মান অপরিবর্তিত থাকে (Constant)।
const personName = "Karim Khan";
console.log("const ব্যবহার করে নাম:", personName); 

/* // const ভেরিয়েবলের মান পরিবর্তন করার চেষ্টা করলে Error আসবে:
// personName = "Rahim Khan"; // TypeError: Assignment to constant variable.
*/

// 3. (Avoid) var ব্যবহার করে ভেরিয়েবল ঘোষণা:
// আধুনিক কোডে var এড়িয়ে চলা উচিত।
var oldAge = 30;
console.log("var ব্যবহার করে বয়স (এড়িয়ে চলুন):", oldAge);
