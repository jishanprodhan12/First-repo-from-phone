// QN-002: অ্যারে ম্যাপ (map) মেথড ব্যবহার।
// আপনার কাছে 'productPrices' নামে একটি অ্যারে আছে যার মান [100, 250, 450, 1000]।
// প্রতিটি মূল্যের উপর 10% ট্যাক্স (Tax) যোগ করে একটি নতুন অ্যারে তৈরি করতে হবে।
// আপনাকে একটি অ্যারো ফাংশন ব্যবহার করে 'map' মেথডটি প্রয়োগ করতে হবে।
// নতুন অ্যারেটির নাম হবে 'pricesWithTax'।
// 'pricesWithTax' ভেরিয়েবলটি কনসোলে প্রিন্ট করার জন্য প্রয়োজনীয় কোড
// QN-002: অ্যারে ম্যাপ (map) মেথড ব্যবহার।
const productPrices = [100, 250, 450, 1000];

// map() মেথডটি প্রতিটি এলিমেন্টের উপর পুনরাবৃত্তি করে এবং একটি নতুন অ্যারে রিটার্ন করে।
const pricesWithTax = productPrices.map((price) => price + (price * 0.10));

console.log(pricesWithTax);
// আউটপুট: [ 110, 275, 495, 1100 ]
